    private void jToggleButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButtonSalvarActionPerformed
        JComponent jComponent[] = {};
        JLabel  jLabel[]        = {};
        String validadores[]           = {"Empty", "Empty", "Empty"};
        
        Validador validador = new Validador(jComponent, jLabel, validadores);
        boolean formValido = validador.validar();
        
        if(formValido){
            //Inserir o novo pedido
            pedido.setEndereco((Endereco) jListEnderecos.getSelectedValue());
            pedido.setFuncionario(Sessao.getInstance().getFuncionario());

            PedidoDAO pedidoDAO = new PedidoDAO();
            pedidoDAO.inserir(pedido);

            //Atualiza o jDesktopPane
           ProdutoListar produtoListar = new ProdutoListar(jDesktopPane);
           produtoListar.setVisible(true);
           jDesktopPane.removeAll();
           jDesktopPane.add(produtoListar);

            JOptionPane.showMessageDialog(null, "Pedido " + pedido.getId() + " cadastrado com sucesso");
           //Fecha jframe
           this.setVisible(false);
           this.dispose();
        }
    }//GEN-LAST:event_jToggleButtonSalvarActionPerformed

